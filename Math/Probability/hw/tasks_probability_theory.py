# ## Задача 1. Лотерея
#
# Условие:
# В лотерее разыгрывается 3 книги. Всего в урне 50 билетов. 
# Первый подошедший к урне вынимает билет. 
# Найти вероятность того, что билет окажется выигрышным.
#
# Решение:
# По формуле классической вероятности P(A) = m/n, где:
# n = 50 (общее число билетов) - число элементарных исходов
# m = 3 (число выигрышных билетов) - число благоприятных исходов
#
# P(A) = m/n = 3/50 = 0.06 = 6%
#
# Ответ: вероятность выигрыша составляет 6%
#
#
