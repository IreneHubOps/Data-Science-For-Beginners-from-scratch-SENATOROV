# - Порядок НЕ ВАЖЕН (неважно кто первый, кто второй)
# - Повторений нет (один студент не может быть выбран дважды)
#
# C²₂₀ = 20!/2!(20-2)! = 20!/(2! × 18!) = (20 × 19)/(2 × 1) = 190
#
# Ответ: 190 способов
#
