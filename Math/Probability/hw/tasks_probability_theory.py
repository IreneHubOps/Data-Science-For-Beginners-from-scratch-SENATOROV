# # Задачи по комбинаторике
#
# ## Задача 1.
#
# Условие:
# В комнате имеется 6 лампочек, каждая со своим выключателем. Сколькими способами можно освещать комнату?
#
# Решение:
#
# Это задача на правило умножения, потому что мы имеем:
# Независимые действия (включение/выключение)
# Для каждого действия фиксированное число вариантов (2)
# Возможность повторения состояний
#
# 2 × 2 × 2 × 2 × 2 × 2 = 2⁶ = 64 способа
#
# Решение через сочетания:
# 1. Включена 0 лампочек: C⁰₆ = 1 способ
# 2. Включена 1 лампочка: C¹₆ = 6 способов
# 3. Включены 2 лампочки: C²₆ = 15 способов
# 4. Включены 3 лампочки: C³₆ = 20 способов
# 5. Включены 4 лампочки: C⁴₆ = 15 способов
# 6. Включены 5 лампочек: C⁵₆ = 6 способов
# 7. Включены 6 лампочек: C⁶₆ = 1 способ
#
# Всего: 1 + 6 + 15 + 20 + 15 + 6 + 1 = 64 способа
#
# Ответ: искомое число способов освещения комнаты = 64.
#
