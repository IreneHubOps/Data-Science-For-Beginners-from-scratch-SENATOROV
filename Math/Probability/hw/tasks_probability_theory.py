# # Задачи на теоремы сложения и умножения вероятностей
#
# ## Задача 1. 
#
# Условие: Для сигнализации об аварии установлены два независимо работающих сигнализатора. Вероятность того, что при аварии сигнализатор сработает, равна 0,95 для первого сигнализатора и 0,9 для второго. Найти вероятности того, что: а) при аварии сработает только один сигнализатор; б) сработает хотя бы один сигнализатор
#
# Решение:
#
# Найдем противоположные события:
# p₁ = 0.95 (вероятность срабатывания первого сигнализатора)
# q₁ = 1 - 0.95 = 0.05 (вероятность НЕсрабатывания первого сигнализатора)
# p₂ = 0.90 (вероятность срабатывания второго сигнализатора)
# q₂ = 1 - 0.90 = 0.10 (вероятность НЕсрабатывания второго сигнализатора)
#
# События называются независимыми, если наступление одного не влияет на вероятность наступления другого.
# События называются несовместными, если они не могут произойти одновременно в одном испытании.
# В задаче сигнализаторы работают независимо друг от друга. Значит это независимые события.
#
# Применяем  две теоремы.
# Теорема умножения для независимых событий P(A × B) = P(A) × P(B).
# Теорема сложения для несовместных событий P(A + B) = P(A) + P(B).
#
# а) Найдем вероятность того, что при аварии сработает только один сигнализатор. 
#
# P(A) = P(p₁) × P(q₂) + P(p₂) × P(q₁) = 0.95 × 0.10 + 0.05 × 0.90 = 0.095 + 0.045 = 0.14
#
# б) Найдем вероятность того, что при аварии сработает хотя бы один сигнализатор. 
# Первый способ решения:
# "Хотя бы один" включает в себя три случая:
# Сработал только первый -  P(p₁) × P(q₂)
# Сработал только второй -  P(p₂) × P(q₁)
# Сработали оба -  P(p₁) × P(p₂)
#
# P(B) = P(p₁) × P(q₂) + P(p₂) × P(q₁) + P(p₁) × P(p₂) = 0.95 × 0.10 + 0.05 × 0.90 + 0.95 × 0.90 = 0.095 + 0.045 + 0.855 = 0.995
#
# Второй способ решения:
#
# Ни один + Хотя бы один = 1
#
# Сначала найдем вероятность того, что оба сигнализатора не сработают (ни один).
# P(C) = P(q₁) × P(q₂) = 0.05 × 0.10 = 0.005
# По теореме сложения для противоположных событий P(B) = 1 - P(C)
# P(B) = 1 - P(C) = 1 - 0.005 = 0.995
#
# Ответ:
# вероятность того, что при аварии сработает только один сигнализатор, составляет 0.14, 
# вероятность того, что сработает хотя бы один сигнализатор, составляет 0.995.
#
#
# ## Задача 2.
#
# Условие:
# Отдел технического контроля проверяет изделия на стандартность.
# Вероятность того, что изделие стандартно, равна 0,9. 
# Найти вероятность того, что из двух проверенных изделий: 
# а) только одно стандартное; 
# б) хотя бы одно стандартное.
#
# Решение:
# Пусть:
# p = 0.9 - вероятность того, что изделие стандартное
# q = 1 - p = 0.1 - вероятность того, что изделие нестандартное
#
# а) Вероятность того, что только одно стандартное:
#    По теореме сложения несовместных событий и умножения независимых событий:
#    P(только одно) = P(первое станд., второе нет) + P(первое нет, второе станд.)
#    P(только одно) = p × q + q × p = 0.9 × 0.1 + 0.1 × 0.9 = 0.18
#
# б) Вероятность того, что хотя бы одно стандартное:
#    Найдем через противоположное событие (оба нестандартные):
#    P(хотя бы одно) = 1 - P(оба нестандартные)
#    P(хотя бы одно) = 1 - q × q = 1 - 0.1 × 0.1 = 1 - 0.01 = 0.99
#
# Ответ: 
# а) вероятность того, что только одно изделие стандартное = 0.18 или 18%
# б) вероятность того, что хотя бы одно изделие стандартное = 0.99 или 99%
#
# ## Задача 3.
#
# Условие:
# Вероятность одного попадания в цель при одном залпе из двух орудий равна 0,38.
# Вероятность попадания вторым орудием p₂ = 0,8.
# Найти:
# 1. Вероятность поражения цели первым орудием (p₁)
# 2. Вероятность поражения цели хотя бы одним из орудий
#
# Решение:
# 1. Найдем вероятность попадания первым орудием (p₁):
#    
#    Вероятность одного попадания (только первое или только второе орудие):
#    P(одно попадание) = p₁q₂ + q₁p₂ = 0.38
#    где: 
#    q₂ = 1 - p₂ = 1 - 0.8 = 0.2 (вероятность промаха вторым орудием)
#    q₁ = 1 - p₁ (вероятность промаха первым орудием)
#
#    Подставляем:
#    p₁ × 0.2 + (1 - p₁) × 0.8 = 0.38
#    0.2p₁ + 0.8 - 0.8p₁ = 0.38
#    -0.6p₁ = -0.42
#    p₁ = 0.7
#
# 2. Найдем вероятность поражения цели хотя бы одним орудием:
#    
#    Через противоположное событие (промах обоими орудиями):
#    P(хотя бы одно) = 1 - P(ни одного)
#    P(хотя бы одно) = 1 - q₁ × q₂
#    P(хотя бы одно) = 1 - (0.3 × 0.2)
#    P(хотя бы одно) = 1 - 0.06 = 0.94
#
# Проверка:
# p₁ = 0.7 - реалистичное значение (0 ≤ p₁ ≤ 1)
# Итоговая вероятность 0.94 < 1
#
# Ответ:
# 1. Вероятность попадания первым орудием = 0.7 или 70%
# 2. Вероятность поражения цели хотя бы одним орудием = 0.94 или 94%
#
