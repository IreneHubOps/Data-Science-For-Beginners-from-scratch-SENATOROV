# # –°—Ä–∞–≤–Ω–µ–Ω–∏–µ –±–∏–Ω–æ–º–∏–∞–ª—å–Ω–æ–≥–æ –∏ –Ω–æ—Ä–º–∞–ª—å–Ω–æ–≥–æ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–π
#
# ## –ë–∏–Ω–æ–º–∏–∞–ª—å–Ω–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ
# –ì—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ –≤–∫–ª—é—á–∞–µ—Ç:
# - PMF (Probability Mass Function - —Ñ—É–Ω–∫—Ü–∏—è –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏)
#     - –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –∫–∞–∂–¥–æ–≥–æ –æ—Ç–¥–µ–ª—å–Ω–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è
#     - –ü—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∞ —Å—Ç–æ–ª–±—á–∞—Ç–æ–π –¥–∏–∞–≥—Ä–∞–º–º–æ–π
#     - –î–∏—Å–∫—Ä–µ—Ç–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è (Œæ‚ÇÅ,Œæ‚ÇÇ,Œæ‚ÇÉ,Œæ‚ÇÑ,Œæ‚ÇÖ)
# –ö–∞–∂–¥—ã–π —Å—Ç–æ–ª–±–∏–∫ –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –∫–æ–Ω–∫—Ä–µ—Ç–Ω–æ–≥–æ –∏—Å—Ö–æ–¥–∞
# - –§—É–Ω–∫—Ü–∏—è —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è (CDF - Cumulative Distribution Function)
#     - –°—Ç—É–ø–µ–Ω—á–∞—Ç–∞—è —Ñ—É–Ω–∫—Ü–∏—è
#     - –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –Ω–∞–∫–æ–ø–ª–µ–Ω–Ω—É—é –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å
#     - –í—Å–µ–≥–¥–∞ –≤–æ–∑—Ä–∞—Å—Ç–∞–µ—Ç
#     - –î–æ—Å—Ç–∏–≥–∞–µ—Ç 1 –≤ –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–º –∑–Ω–∞—á–µ–Ω–∏–∏
#
# –§–û–†–ú–£–õ–´:
# p_(x_ = k_) = C(n_,k_) * p_^k_ * (1-p_)^(n_-k_)
# –≥–¥–µ:
# - n_ - —á–∏—Å–ª–æ –∏—Å–ø—ã—Ç–∞–Ω–∏–π
# - k_ - —á–∏—Å–ª–æ —É—Å–ø–µ—Ö–æ–≤
# - p_ - –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —É—Å–ø–µ—Ö–∞ –≤ –∫–∞–∂–¥–æ–º –∏—Å–ø—ã—Ç–∞–Ω–∏–∏

# ## –ù–æ—Ä–º–∞–ª—å–Ω–æ–µ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏–µ
# –ì—Ä–∞—Ñ–∏—á–µ—Å–∫–æ–µ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª–µ–Ω–∏–µ –≤–∫–ª—é—á–∞–µ—Ç:
# - PDF (Probability Density Function) - —Ñ—É–Ω–∫—Ü–∏—è –ø–ª–æ—Ç–Ω–æ—Å—Ç–∏ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç–∏
#     - –ù–µ–ø—Ä–µ—Ä—ã–≤–Ω–∞—è –∫–æ–ª–æ–∫–æ–ª–æ–æ–±—Ä–∞–∑–Ω–∞—è –∫—Ä–∏–≤–∞—è
#     - –°–∏–º–º–µ—Ç—Ä–∏—á–Ω–∞ –æ—Ç–Ω–æ—Å–∏—Ç–µ–ª—å–Ω–æ —Å—Ä–µ–¥–Ω–µ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è (Œº)
#     - –†–∞–∑–Ω—ã–µ –∫—Ä–∏–≤—ã–µ –¥–ª—è —Ä–∞–∑–Ω—ã—Ö –ø–∞—Ä–∞–º–µ—Ç—Ä–æ–≤ (Œº –∏ œÉ)
#     - –ü–æ–∫–∞–∑–∞–Ω—ã –∑–Ω–∞—á–µ–Ω–∏—è 150, 168, 200 (–≤–æ–∑–º–æ–∂–Ω–æ, —Ä–æ—Å—Ç –≤ —Å–º)
# - CDF (Cumulative Distribution Function) - —Ñ—É–Ω–∫—Ü–∏—è —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è
#     - –ü–ª–∞–≤–Ω–∞—è S-–æ–±—Ä–∞–∑–Ω–∞—è –∫—Ä–∏–≤–∞—è
#     - –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —Ç–æ–≥–æ, —á—Ç–æ –≤–µ–ª–∏—á–∏–Ω–∞ –º–µ–Ω—å—à–µ –∑–∞–¥–∞–Ω–Ω–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è
#     - –°—Ç—Ä–µ–º–∏—Ç—Å—è –∫ 1 –ø—Ä–∏ —É–≤–µ–ª–∏—á–µ–Ω–∏–∏ x_
#
# –§–û–†–ú–£–õ–´:
# f(x_) = (1/(œÉ‚àö(2œÄ))) * e^(-(x_-Œº)¬≤/(2œÉ¬≤)),  –≥–¥–µ e ‚âà 2.71828... (—á–∏—Å–ª–æ –≠–π–ª–µ—Ä–∞)
# –≥–¥–µ:
# - Œº (–º—é) - –º–∞—Ç–µ–º–∞—Ç–∏—á–µ—Å–∫–æ–µ –æ–∂–∏–¥–∞–Ω–∏–µ
# - œÉ (—Å–∏–≥–º–∞) - —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–æ–µ –æ—Ç–∫–ª–æ–Ω–µ–Ω–∏–µ

# ## –°—Ä–∞–≤–Ω–µ–Ω–∏–µ
# ### –ù–µ–ø—Ä–µ—Ä—ã–≤–Ω–æ—Å—Ç—å:
# - –ë–∏–Ω–æ–º–∏–∞–ª—å–Ω–æ–µ: –¥–∏—Å–∫—Ä–µ—Ç–Ω–æ–µ (—Å—Ç–æ–ª–±–∏–∫–∏)
# - –ù–æ—Ä–º–∞–ª—å–Ω–æ–µ: –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω–æ–µ (–≥–ª–∞–¥–∫–∞—è –∫—Ä–∏–≤–∞—è)
# ### –§–æ—Ä–º–∞:
# - –ë–∏–Ω–æ–º–∏–∞–ª—å–Ω–æ–µ: –º–æ–∂–µ—Ç –±—ã—Ç—å –∞—Å–∏–º–º–µ—Ç—Ä–∏—á–Ω—ã–º
# - –ù–æ—Ä–º–∞–ª—å–Ω–æ–µ: –≤—Å–µ–≥–¥–∞ —Å–∏–º–º–µ—Ç—Ä–∏—á–Ω–æ
# ### –ü—Ä–∏–º–µ–Ω–µ–Ω–∏–µ:
# - –ë–∏–Ω–æ–º–∏–∞–ª—å–Ω–æ–µ: –¥–ª—è –ø–æ–¥—Å—á–µ—Ç–∞ —É—Å–ø–µ—Ö–æ–≤ –≤ —Å–µ—Ä–∏–∏ –∏—Å–ø—ã—Ç–∞–Ω–∏–π
# - –ù–æ—Ä–º–∞–ª—å–Ω–æ–µ: –¥–ª—è –æ–ø–∏—Å–∞–Ω–∏—è –ø—Ä–∏—Ä–æ–¥–Ω—ã—Ö —è–≤–ª–µ–Ω–∏–π (—Ä–æ—Å—Ç, –≤–µ—Å, —Ç–µ–º–ø–µ—Ä–∞—Ç—É—Ä–∞)
#
# ### –§—É–Ω–∫—Ü–∏—è —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è:
# - –ë–∏–Ω–æ–º–∏–∞–ª—å–Ω–æ–µ: —Å—Ç—É–ø–µ–Ω—á–∞—Ç–∞—è
# - –ù–æ—Ä–º–∞–ª—å–Ω–æ–µ: –≥–ª–∞–¥–∫–∞—è
#
# ### –ü—Ä–∞–∫—Ç–∏—á–µ—Å–∫–æ–µ –ø—Ä–∏–º–µ–Ω–µ–Ω–∏–µ:
# - –ë–∏–Ω–æ–º–∏–∞–ª—å–Ω–æ–µ: –ø–æ–¥–±—Ä–∞—Å—ã–≤–∞–Ω–∏–µ –º–æ–Ω–µ—Ç—ã, —Ç–µ—Å—Ç—ã —Å –≤—ã–±–æ—Ä–æ–º –æ—Ç–≤–µ—Ç–∞
# - –ù–æ—Ä–º–∞–ª—å–Ω–æ–µ: –∏–∑–º–µ—Ä–µ–Ω–∏–µ —Ä–æ—Å—Ç–∞ –ª—é–¥–µ–π, –æ—Ü–µ–Ω–∫–∞ –ø–æ–≥—Ä–µ—à–Ω–æ—Å—Ç–µ–π –∏–∑–º–µ—Ä–µ–Ω–∏–π

# # –°–≤–æ–π—Å—Ç–≤–∞ —Ñ—É–Ω–∫—Ü–∏–∏ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è
#
# ## –û—Å–Ω–æ–≤–Ω—ã–µ —Å–≤–æ–π—Å—Ç–≤–∞ F(x_)
#
# - –û–≥—Ä–∞–Ω–∏—á–µ–Ω–Ω–æ—Å—Ç—å
# 0 ‚â§ F(x_) ‚â§ 1
# –≠—Ç–æ —Å–≤–æ–π—Å—Ç–≤–æ —Å–ª–µ–¥—É–µ—Ç –∏–∑ —Ç–æ–≥–æ, —á—Ç–æ F(x_) –µ—Å—Ç—å –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å
#
# - –ú–æ–Ω–æ—Ç–æ–Ω–Ω–æ—Å—Ç—å
# F(x_) - –Ω–µ—É–±—ã–≤–∞—é—â–∞—è —Ñ—É–Ω–∫—Ü–∏—è
# –ï—Å–ª–∏ x‚ÇÅ < x‚ÇÇ, —Ç–æ F(x‚ÇÅ) ‚â§ F(x‚ÇÇ)
#
# - –ü—Ä–µ–¥–µ–ª—å–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è
# F(-‚àû) = lim[x‚Üí-‚àû] F(x_) = 0
# F(+‚àû) = lim[x‚Üí+‚àû] F(x_) = 1
#
# -–í–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –ø–æ–ø–∞–¥–∞–Ω–∏—è –≤ –∏–Ω—Ç–µ—Ä–≤–∞–ª
# - p_(Œ± < Œæ < Œ≤) = F(Œ≤) - F(Œ±)
# –≠—Ç–æ —Å–≤–æ–π—Å—Ç–≤–æ —Å–≤—è–∑—ã–≤–∞–µ—Ç —Ñ—É–Ω–∫—Ü–∏—é —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è –∏ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å –ø–æ–ø–∞–¥–∞–Ω–∏—è –≤ –∏–Ω—Ç–µ—Ä–≤–∞–ª
#
# - –ù–µ–ø—Ä–µ—Ä—ã–≤–Ω–æ—Å—Ç—å —Å–ª–µ–≤–∞
# p_(Œæ = x‚ÇÄ) = F(x‚ÇÄ + 0) - F(x‚ÇÄ) = 0
# F(x_) –Ω–µ–ø—Ä–µ—Ä—ã–≤–Ω–∞ —Å–ª–µ–≤–∞ –≤ –ª—é–±–æ–π —Ç–æ—á–∫–µ x_, —Ç.–µ. F(x_ - 0) = F(x_)
#
# ## –ö–ª–∞—Å—Å–∏—Ñ–∏–∫–∞—Ü–∏—è —Å–ª—É—á–∞–π–Ω—ã—Ö –≤–µ–ª–∏—á–∏–Ω
#
# 1. –î–∏—Å–∫—Ä–µ—Ç–Ω—ã–µ –°–í
# –ú–Ω–æ–∂–µ—Å—Ç–≤–æ –∑–Ω–∞—á–µ–Ω–∏–π {x‚ÇÅ, x‚ÇÇ, ..., x‚Çô, ...}
# –ò–∑–æ–ª–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è –∏–ª–∏ —Å—á–µ—Ç–Ω–æ–µ –º–Ω–æ–∂–µ—Å—Ç–≤–æ –∑–Ω–∞—á–µ–Ω–∏–π
# –ú–æ–∂–Ω–æ –ø—Ä–æ–Ω—É–º–µ—Ä–æ–≤–∞—Ç—å –≤—Å–µ –≤–æ–∑–º–æ–∂–Ω—ã–µ –∑–Ω–∞—á–µ–Ω–∏—è
#
