n_, p_ = 20, 0.5  # –ø–∞—Ä–∞–º–µ—Ç—Ä—ã: n_ –∏—Å–ø—ã—Ç–∞–Ω–∏–π, p_ –≤–µ—Ä–æ—è—Ç–Ω–æ—Å—Ç—å —É—Å–ø–µ—Ö–∞
